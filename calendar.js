// ================= АВТООПРЕДЕЛЕНИЕ ТЕКУЩЕЙ ДАТЫ =================
function getCurrentDateInRange() {
    const now = new Date();
    const year = now.getFullYear();
    const month = now.getMonth();
    const day = now.getDate();
    
    // Если сегодня в пределах мая-июня 2026
    if (year === 2026 && (month === 4 || month === 5)) {
        const maxDay = month === 4 ? 31 : 30;
        const safeDay = Math.min(day, maxDay);
        return new Date(2026, month, safeDay);
    }
    
    // Иначе возвращаем 1 мая 2026
    return new Date(2026, 4, 1);
}

function formatDateForDB(date) {
    const year = date.getFullYear();
    const month = String(date.getMonth() + 1).padStart(2, '0');
    const day = String(date.getDate()).padStart(2, '0');
    return `${year}-${month}-${day}`;
}

function findWeekForDate(targetDate) {
    for (let i = 0; i < WEEKS.length; i++) {
        const start = new Date(WEEKS[i].startDate);
        const end = new Date(WEEKS[i].endDate);
        if (targetDate >= start && targetDate <= end) return i;
    }
    return 0;
}

// ================= ФУНКЦИИ РЕНДЕРИНГА =================
function getCurrentWeekDays() {
    const weekDays = [];
    const daysOfWeek = ['ВС', 'ПН', 'ВТ', 'СР', 'ЧТ', 'ПТ', 'СБ'];
    const monthNames = ['ЯНВ', 'ФЕВ', 'МАР', 'АПР', 'МАЙ', 'ИЮН', 'ИЮЛ', 'АВГ', 'СЕН', 'ОКТ', 'НОЯ', 'ДЕК'];
    
    if (!WEEKS[currentWeekOffset]) return weekDays;
    
    const week = WEEKS[currentWeekOffset];
    const startDate = new Date(week.startDate);
    const currentUserDate = getCurrentDateInRange();
    const userDateString = formatDateForDB(currentUserDate);
    
    for (let i = 0; i < 7; i++) {
        const currentDate = new Date(startDate);
        currentDate.setDate(startDate.getDate() + i);
        const dateString = formatDateForDB(currentDate);
        
        weekDays.push({
            date: currentDate,
            dateString: dateString,
            weekday: daysOfWeek[currentDate.getDay()],
            day: currentDate.getDate(),
            month: monthNames[currentDate.getMonth()],
            isUserDate: dateString === userDateString
        });
    }
    return weekDays;
}

function renderMiniCalendar() {
    const dateGrid = document.getElementById('dateGrid');
    if (!dateGrid) return;
    
    const weekDays = getCurrentWeekDays();
    if (!weekDays.length) return;
    
    dateGrid.innerHTML = weekDays.map(dayInfo => createDateBoxHTML(dayInfo)).join('');
    
    document.querySelectorAll('.date-box').forEach(box => {
        box.addEventListener('click', handleDateClick);
    });
}

function createDateBoxHTML(dayInfo) {
    const event = EVENT_DB[dayInfo.dateString];
    let indicatorClass = '';
    
    if (event) {
        indicatorClass = event.type === 'incident' ? `has-${event.level}` : 'has-holiday';
    }
    
    const userDateClass = dayInfo.isUserDate ? 'user-date' : '';
    
    return `
        <div class="date-box ${indicatorClass} ${userDateClass}" data-date="${dayInfo.dateString}">
            <div class="date-weekday">${dayInfo.weekday}</div>
            <div class="date-day">${dayInfo.day}</div>
            <div class="date-month">${dayInfo.month}</div>
        </div>
    `;
}

function handleDateClick(event) {
    const dateBox = event.currentTarget;
    const dateString = dateBox.dataset.date;
    
    SELECTED_DATE = new Date(dateString);
    
    document.querySelectorAll('.date-box').forEach(box => box.classList.remove('active'));
    dateBox.classList.add('active');
    
    renderEventDetails(dateString);
}

