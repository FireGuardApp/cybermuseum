function updateWeekLabel() {
    const weekLabel = document.getElementById('weekLabel');
    if (weekLabel && WEEKS[currentWeekOffset]) {
        weekLabel.textContent = WEEKS[currentWeekOffset].label;
    }
}

function changeWeek(direction) {
    const prevOffset = currentWeekOffset;
    
    if (direction === 'prev' && currentWeekOffset > 0) {
        currentWeekOffset--;
    } else if (direction === 'next' && currentWeekOffset < WEEKS.length - 1) {
        currentWeekOffset++;
    }
    
    if (prevOffset !== currentWeekOffset) {
        updateWeekLabel();
        renderMiniCalendar();
        updateArrowStates();
        
        // –ü–æ–¥—Å–≤–µ—á–∏–≤–∞–µ–º –¥–∞—Ç—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–ª–∏ –ø–µ—Ä–≤—É—é –¥–∞—Ç—É
        setTimeout(() => {
            const userDate = getUserDateInCalendar().dateString;
            const userBox = document.querySelector(`.date-box[data-date="${userDate}"]`);
            
            if (userBox) {
                userBox.click();
            } else if (document.querySelector('.date-box')) {
                document.querySelector('.date-box').click();
            }
        }, 100);
    }
}

function updateArrowStates() {
    const prevArrow = document.querySelector('.week-arrow.prev');
    const nextArrow = document.querySelector('.week-arrow.next');
    
    if (prevArrow) {
        prevArrow.classList.toggle('disabled', currentWeekOffset === 0);
    }
    if (nextArrow) {
        nextArrow.classList.toggle('disabled', currentWeekOffset === WEEKS.length - 1);
    }
}

