function findCurrentWeek(targetDate) {
    const target = new Date(targetDate);
    
    for (let i = 0; i < WEEKS.length; i++) {
        const weekStart = new Date(WEEKS[i].startDate);
        const weekEnd = new Date(WEEKS[i].endDate);
        
        if (target >= weekStart && target <= weekEnd) {
            currentWeekOffset = i;
            return;
        }
    }
    
    // –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏, –±–µ—Ä–µ–º –ø–µ—Ä–≤—É—é –Ω–µ–¥–µ–ª—é
    currentWeekOffset = 0;
}

