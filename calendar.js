function getCurrentWeekDays() {
    const weekDays = [];
    const daysOfWeek = ['–í–°', '–ü–ù', '–í–¢', '–°–†', '–ß–¢', '–ü–¢', '–°–ë'];
    
    if (!WEEKS[currentWeekOffset]) return weekDays;
    
    const week = WEEKS[currentWeekOffset];
    const startDate = new Date(week.startDate);
    
    // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º 7 –¥–Ω–µ–π –Ω–µ–¥–µ–ª–∏
    for (let i = 0; i < 7; i++) {
        const currentDate = new Date(startDate);
        currentDate.setDate(startDate.getDate() + i);
        
        const dateString = formatDateForDB(currentDate);
        const monthNames = ['–Ø–ù–í', '–§–ï–í', '–ú–ê–†', '–ê–ü–†', '–ú–ê–ô', '–ò–Æ–ù', '–ò–Æ–õ', '–ê–í–ì', '–°–ï–ù', '–û–ö–¢', '–ù–û–Ø', '–î–ï–ö'];
        
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —ç—Ç–∞ –¥–∞—Ç–∞ –¥–∞—Ç–æ–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        const userDate = getUserDateInCalendar().dateString;
        const isUserDate = dateString === userDate;
        
        weekDays.push({
            date: currentDate,
            dateString: dateString,
            weekday: daysOfWeek[currentDate.getDay()],
            day: currentDate.getDate(),
            month: monthNames[currentDate.getMonth()],
            isUserDate: isUserDate
        });
    }
    
    return weekDays;
}

