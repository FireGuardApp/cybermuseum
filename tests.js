let currentTest = null;
let currentQuestionIndex = 0;
let score = 0;
let answerLocked = false;

document.addEventListener('DOMContentLoaded', initTests);

function initTests() {
  const testsList = document.getElementById('tests-list');
  const quizSection = document.getElementById('quiz-section');
  const resultSection = document.getElementById('result-section');
  const quizTitle = document.getElementById('quiz-title');
  const quizDescription = document.getElementById('quiz-description');
  const progressText = document.getElementById('test-progress');
  const questionText = document.getElementById('question-text');
  const answersBox = document.getElementById('answers');
  const feedbackBox = document.getElementById('feedback-box');
  const nextBtn = document.getElementById('next-btn');
  const backBtns = document.querySelectorAll('[data-action="back-to-tests"]');
  const restartBtn = document.getElementById('restart-btn');
  const testsGrid = document.getElementById('tests-grid');

  // Если tests.js случайно подключили на странице без блока тестов,
  // ничего не ломаем и просто не запускаем код тестов.
  if (!testsList || !quizSection || !resultSection || !testsGrid || !nextBtn || !restartBtn) {
    return;
  }

  function renderTestCards() {
    testsGrid.innerHTML = '';

    TESTS.forEach((test) => {
      const card = document.createElement('article');
      card.className = `game-card card ${test.cardClass}`;
      card.innerHTML = `
        <div class="game-icon">${test.icon}</div>
        <h3 class="card-title">${escapeHtml(test.title)}</h3>
        <p class="card-text">${escapeHtml(test.cardText || test.shortDescription)}</p>
        <button class="game-btn" data-test-id="${test.id}" type="button">${escapeHtml(test.buttonText || 'Пройти тест')}</button>
      `;
      testsGrid.appendChild(card);
    });

    testsGrid.addEventListener('click', (event) => {
      const btn = event.target.closest('[data-test-id]');
      if (!btn) return;
      startTest(btn.dataset.testId);
    });
  }

function startTest(testId) {
  currentTest = TESTS.find((test) => test.id === testId);
  if (!currentTest) return;

  currentQuestionIndex = 0;
  score = 0;
  answerLocked = false;

  testsList.classList.add('hidden');
  resultSection.classList.add('hidden');
  quizSection.classList.remove('hidden');

  quizTitle.textContent = currentTest.title;
  quizDescription.textContent = currentTest.shortDescription;
  showQuestion();

  quizSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

  function showQuestion() {
    answerLocked = false;
    feedbackBox.className = 'feedback-box hidden';
    feedbackBox.textContent = '';
    nextBtn.disabled = true;
    nextBtn.textContent = currentQuestionIndex === currentTest.questions.length - 1 ? 'Показать результат' : 'Следующий вопрос';

    const question = currentTest.questions[currentQuestionIndex];
    progressText.textContent = `Вопрос ${currentQuestionIndex + 1} из ${currentTest.questions.length}`;
    questionText.textContent = question.question;
    answersBox.innerHTML = '';

    question.answers.forEach((answer) => {
      const btn = document.createElement('button');
      btn.className = 'answer';
      btn.type = 'button';
      btn.dataset.answerId = answer.id;
      btn.innerHTML = `<span class="answer-letter">${escapeHtml(answer.id)}</span> ${escapeHtml(answer.text)}`;
      btn.addEventListener('click', () => selectAnswer(btn, question));
      answersBox.appendChild(btn);
    });
  }

  function selectAnswer(button, question) {
    if (answerLocked) return;
    answerLocked = true;

    const selected = button.dataset.answerId;
    const isCorrect = selected === question.correct;
    if (isCorrect) score++;

    const answerButtons = answersBox.querySelectorAll('.answer');
    answerButtons.forEach((btn) => {
      btn.disabled = true;
      if (btn.dataset.answerId === question.correct) btn.classList.add('correct');
    });

    if (!isCorrect) button.classList.add('wrong');

    feedbackBox.classList.remove('hidden');
    feedbackBox.classList.add(isCorrect ? 'correct' : 'wrong');
    feedbackBox.textContent = isCorrect ? question.feedbackCorrect : question.feedbackWrong;
    nextBtn.disabled = false;
  }

  function nextQuestion() {
    if (currentQuestionIndex < currentTest.questions.length - 1) {
      currentQuestionIndex++;
      showQuestion();
      return;
    }
    showResult();
  }

function showResult() {
  quizSection.classList.add('hidden');
  resultSection.classList.remove('hidden');

  const result = currentTest.results.find((item) => score >= item.min && score <= item.max) || currentTest.results[currentTest.results.length - 1];

  document.getElementById('result-title').textContent = result.level;
  document.getElementById('result-score').textContent = `${score} из ${currentTest.questions.length}`;
  document.getElementById('result-text').textContent = result.text;

  resultSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

function backToTests() {
  quizSection.classList.add('hidden');
  resultSection.classList.add('hidden');
  testsList.classList.remove('hidden');

  testsList.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

  nextBtn.addEventListener('click', nextQuestion);
  restartBtn.addEventListener('click', () => currentTest && startTest(currentTest.id));
  backBtns.forEach((btn) => btn.addEventListener('click', backToTests));
  renderTestCards();

  // Можно открыть конкретный тест ссылкой: tests.html?test=fraud / security / malware
  const params = new URLSearchParams(window.location.search);
  const testFromUrl = params.get('test');
  if (testFromUrl) startTest(testFromUrl);
}

function escapeHtml(value) {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');
}

