// Обновление timeline
function updateTimeline() {
    const timelineTrack = document.getElementById('timelineTrack');
    if (!timelineTrack) return;
    
    timelineTrack.innerHTML = '';
    
    // Фильтруем события по текущему месяцу и активному фильтру
    const timelineEvents = [];
    
    for (let day = 1; day <= 31; day++) {
        const date = new Date(currentYear, currentMonth, day);
        const dateString = formatDateForDB(date);
        const event = events[dateString];
        
        if (event && checkEventFilter(event)) {
            timelineEvents.push({
                date: dateString,
                event: event
            });
        }
    }
    
    // Ограничиваем количество событий для timeline
    const limitedEvents = timelineEvents.slice(0, 20);
    
    // Добавляем события в timeline
    limitedEvents.forEach(item => {
        const timelineItem = document.createElement('div');
        timelineItem.className = `timeline-item ${item.event.type === 'incident' ? item.event.level : 'holiday'}`;
        
        timelineItem.innerHTML = `
            <div class="timeline-date">${formatDateDisplay(item.date)}</div>
            <div class="timeline-title">${item.event.title}</div>
            <div class="timeline-type">${item.event.type === 'incident' ? 'Инцидент' : 'Событие'}</div>
        `;
        
        timelineItem.addEventListener('click', () => {
            selectDate(item.date);
            
            // Прокручиваем к выбранному дню в календаре
            scrollToDateInCalendar(item.date);
        });
        
        timelineTrack.appendChild(timelineItem);
    });
}

// Инициализация тепловой карты
function initHeatmap() {
    const heatmap = document.getElementById('heatmap');
    if (!heatmap) return;
    
    heatmap.innerHTML = '';
    
    // Создаем ячейки для каждого дня месяца
    const daysInMonth = new Date(currentYear, currentMonth + 1, 0).getDate();
    
    for (let day = 1; day <= daysInMonth; day++) {
        const date = new Date(currentYear, currentMonth, day);
        const dateString = formatDateForDB(date);
        const event = events[dateString];
        
        const heatmapDay = document.createElement('div');
        heatmapDay.className = 'heatmap-day';
        
        // Уровень активности (0-7)
        let activityLevel = 0;
        
        if (event) {
            if (event.type === 'incident') {
                switch (event.level) {
                    case 'critical': activityLevel = 6 + (day % 2); break;
                    case 'medium': activityLevel = 4 + (day % 2); break;
                    case 'low': activityLevel = 2 + (day % 2); break;
                }
            } else {
                activityLevel = 1;
            }
        }
        
        heatmapDay.dataset.level = activityLevel;
        
        // Tooltip
        const tooltip = document.createElement('div');
        tooltip.className = 'heatmap-tooltip';
        
        if (event) {
            tooltip.textContent = `${day} ${MONTH_NAMES[currentMonth]}: ${event.title}`;
        } else {
            tooltip.textContent = `${day} ${MONTH_NAMES[currentMonth]}: Нет событий`;
        }
        
        heatmapDay.appendChild(tooltip);
        
        // Обработчик клика
        heatmapDay.addEventListener('click', () => {
            if (event) {
                selectDate(dateString);
            }
        });
        
        heatmap.appendChild(heatmapDay);
    }
}

