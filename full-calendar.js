// –†–µ–Ω–¥–µ—Ä–∏–Ω–≥ –∫–∞–ª–µ–Ω–¥–∞—Ä—è
function renderCalendar() {
    const calendarGrid = document.getElementById('calendarGrid');
    if (!calendarGrid) {
        console.error('–ù–µ –Ω–∞–π–¥–µ–Ω —ç–ª–µ–º–µ–Ω—Ç calendarGrid');
        return;
    }
    
    // –ü–æ–ª—É—á–∞–µ–º –ø–µ—Ä–≤—ã–π –¥–µ–Ω—å –º–µ—Å—è—Ü–∞ –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–Ω–µ–π
    const firstDay = new Date(currentYear, currentMonth, 1);
    const lastDay = new Date(currentYear, currentMonth + 1, 0);
    const daysInMonth = lastDay.getDate();
    const startingDay = (firstDay.getDay() + 6) % 7; // –ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ = 0
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –º–µ—Å—è—Ü–∞
    updateMonthDisplay();
    
    // –û–±–Ω–æ–≤–ª—è–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
    updateStats();
    
    // –û—á–∏—â–∞–µ–º —Å–µ—Ç–∫—É
    calendarGrid.innerHTML = '';
    
    // –î–æ–±–∞–≤–ª—è–µ–º –ø—É—Å—Ç—ã–µ —è—á–µ–π–∫–∏ –¥–ª—è –¥–Ω–µ–π –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ –º–µ—Å—è—Ü–∞
    for (let i = 0; i < startingDay; i++) {
        calendarGrid.appendChild(createEmptyDay());
    }
    
    // –î–æ–±–∞–≤–ª—è–µ–º –¥–Ω–∏ —Ç–µ–∫—É—â–µ–≥–æ –º–µ—Å—è—Ü–∞
    const today = new Date();
    const userDate = getUserDate();
    
    for (let day = 1; day <= daysInMonth; day++) {
        const date = new Date(currentYear, currentMonth, day);
        const dateString = formatDateForDB(date);
        const event = events[dateString];
        
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –¥–µ–Ω—å —Å–µ–≥–æ–¥–Ω—è—à–Ω–∏–º –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        const isUserDate = userDate && 
                          userDate.getDate() === day && 
                          userDate.getMonth() === currentMonth && 
                          userDate.getFullYear() === currentYear;
        
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –ª–∏ –¥–µ–Ω—å —Ñ–∏–ª—å—Ç—Ä—É
        const passesFilter = event ? checkEventFilter(event) : activeFilter === 'all';
        
        calendarGrid.appendChild(createDayElement(day, dateString, event, isUserDate, passesFilter));
    }
    
    // –î–æ–±–∞–≤–ª—è–µ–º –æ—Å—Ç–∞–≤—à–∏–µ—Å—è –ø—É—Å—Ç—ã–µ —è—á–µ–π–∫–∏
    const totalCells = 42;
    const filledCells = startingDay + daysInMonth;
    const remainingCells = totalCells - filledCells;
    
    for (let i = 0; i < remainingCells; i++) {
        calendarGrid.appendChild(createEmptyDay());
    }
}


// –°–æ–∑–¥–∞–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–∞ –¥–Ω—è
function createDayElement(day, dateString, event, isUserDate, passesFilter) {
    const dayElement = document.createElement('div');
    dayElement.className = 'calendar-day';
    
    // –î–æ–±–∞–≤–ª—è–µ–º –∞—Ç—Ä–∏–±—É—Ç data-event –¥–ª—è —Ü–≤–µ—Ç–∞ —Ñ–æ–Ω–∞
    if (event) {
        dayElement.dataset.event = event.type === 'incident' ? event.level : 'holiday';
    }
    
    if (!passesFilter) {
        dayElement.classList.add('filtered-out');
        dayElement.style.opacity = '0.3';
    }
    
    if (isUserDate) {
        dayElement.classList.add('today');
    }
    
    if (selectedDate && selectedDate === dateString) {
        dayElement.classList.add('active');
    }
    
    // –ù–æ–º–µ—Ä –¥–Ω—è
    const dayNumber = document.createElement('div');
    dayNumber.className = 'day-number';
    dayNumber.textContent = day;
    dayElement.appendChild(dayNumber);
    
    // –ò–Ω–¥–∏–∫–∞—Ç–æ—Ä—ã —Å–æ–±—ã—Ç–∏–π
    if (event && passesFilter) {
        const eventIndicators = document.createElement('div');
        eventIndicators.className = 'day-events';
        
        const indicator = document.createElement('div');
        indicator.className = `event-indicator ${event.type === 'incident' ? event.level : 'holiday'}`;
        eventIndicators.appendChild(indicator);
        
        dayElement.appendChild(eventIndicators);
    }
    
    // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–ª–∏–∫–∞ —Å –ø–ª–∞–≤–Ω–æ–π –ø—Ä–æ–∫—Ä—É—Ç–∫–æ–π
    dayElement.addEventListener('click', (e) => {
        if (passesFilter) {
            e.preventDefault();
            selectDate(dateString);
            
            // –ü–ª–∞–≤–Ω–∞—è –ø—Ä–æ–∫—Ä—É—Ç–∫–∞ –∫ –ø–∞–Ω–µ–ª–∏ —Å–æ–±—ã—Ç–∏–π
            setTimeout(() => {
                const eventPanel = document.querySelector('.detailed-event-panel');
                if (eventPanel) {
                    eventPanel.scrollIntoView({ 
                        behavior: 'smooth', 
                        block: 'start',
                        inline: 'nearest'
                    });
                    
                    // –î–æ–±–∞–≤–ª—è–µ–º –≤–∏–∑—É–∞–ª—å–Ω—ã–π —ç—Ñ—Ñ–µ–∫—Ç –ø—Ä–∏ –ø—Ä–æ–∫—Ä—É—Ç–∫–µ
                    eventPanel.style.boxShadow = '0 0 30px rgba(0, 188, 212, 0.5)';
                    setTimeout(() => {
                        eventPanel.style.boxShadow = '';
                    }, 1500);
                }
            }, 300);
        }
    });
    
    // –î–æ–±–∞–≤–ª—è–µ–º tooltip –ø—Ä–∏ –Ω–∞–≤–µ–¥–µ–Ω–∏–∏
    if (event) {
        dayElement.title = event.title;
        
        // –°–æ–∑–¥–∞–µ–º —Ä–∞—Å—à–∏—Ä–µ–Ω–Ω—ã–π tooltip
        dayElement.addEventListener('mouseenter', (e) => {
            const tooltip = document.createElement('div');
            tooltip.className = 'day-tooltip';
            tooltip.innerHTML = `
                <strong>${event.title}</strong><br>
                <small>${event.type === 'incident' ? '–£—Ä–æ–≤–µ–Ω—å —É–≥—Ä–æ–∑—ã: ' + event.levelText : '–°–æ–±—ã—Ç–∏–µ'}</small>
            `;
            tooltip.style.position = 'fixed';
            tooltip.style.left = e.pageX + 15 + 'px';
            tooltip.style.top = e.pageY + 15 + 'px';
            tooltip.style.background = 'rgba(0, 0, 0, 0.9)';
            tooltip.style.color = 'white';
            tooltip.style.padding = '10px';
            tooltip.style.borderRadius = '4px';
            tooltip.style.border = '1px solid var(--accent-blue)';
            tooltip.style.zIndex = '10000';
            tooltip.style.maxWidth = '250px';
            tooltip.style.boxShadow = '0 5px 15px rgba(0,0,0,0.5)';
            document.body.appendChild(tooltip);
            
            dayElement._tooltip = tooltip;
        });
        
        dayElement.addEventListener('mousemove', (e) => {
            if (dayElement._tooltip) {
                dayElement._tooltip.style.left = e.pageX + 15 + 'px';
                dayElement._tooltip.style.top = e.pageY + 15 + 'px';
            }
        });
        
        dayElement.addEventListener('mouseleave', () => {
            if (dayElement._tooltip) {
                document.body.removeChild(dayElement._tooltip);
                delete dayElement._tooltip;
            }
        });
    }
    
    return dayElement;
}

