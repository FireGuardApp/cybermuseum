// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
function updateStats() {
    let total = 0;
    let critical = 0;
    let medium = 0;
    let low = 0;
    let holiday = 0;
    
    // –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º —Å–æ–±—ã—Ç–∏—è –¥–ª—è —Ç–µ–∫—É—â–µ–≥–æ –º–µ—Å—è—Ü–∞
    for (let day = 1; day <= 31; day++) {
        const date = new Date(currentYear, currentMonth, day);
        const dateString = formatDateForDB(date);
        const event = events[dateString];
        
        if (event) {
            total++;
            if (event.type === 'incident') {
                switch (event.level) {
                    case 'critical': critical++; break;
                    case 'medium': medium++; break;
                    case 'low': low++; break;
                }
            } else {
                holiday++;
            }
        }
    }
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –∑–Ω–∞—á–µ–Ω–∏—è –≤ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–µ
    const totalEventsEl = document.getElementById('totalEvents');
    if (totalEventsEl) totalEventsEl.textContent = total;
    
    const criticalStat = document.querySelector('.stat-value.critical');
    if (criticalStat) criticalStat.textContent = critical;
    
    const statNumbers = document.querySelectorAll('.stat-number');
    if (statNumbers.length >= 4) {
        statNumbers[0].textContent = critical;
        statNumbers[1].textContent = medium;
        statNumbers[2].textContent = low;
        statNumbers[3].textContent = holiday;
    }
}

