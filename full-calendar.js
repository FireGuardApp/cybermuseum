function checkEventFilter(event) {
    if (activeFilter === 'all') return true;
    
    if (activeFilter === 'holiday') {
        return event.type === 'holiday';
    }
    
    if (event.type === 'incident') {
        return event.level === activeFilter;
    }
    
    return false;
}

