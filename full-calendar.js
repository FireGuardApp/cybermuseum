// Смена месяца
function changeMonth(delta) {
    currentMonth += delta;
    
    // Корректировка года при переходе через границы
    if (currentMonth < 0) {
        currentMonth = 11;
        currentYear--;
    } else if (currentMonth > 11) {
        currentMonth = 0;
        currentYear++;
    }
    
    // Перерисовываем календарь
    renderCalendar();
    
    // Обновляем timeline
    updateTimeline();
    
    // Обновляем тепловую карту
    initHeatmap();
    
    // Сбрасываем выбранную дату
    selectedDate = null;
    clearEventPanel();
}

