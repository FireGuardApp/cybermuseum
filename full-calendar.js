// Создание HTML для события
function createEventHTML(event, dateString) {
    const date = new Date(dateString);
    const formattedDate = `${date.getDate().toString().padStart(2, '0')}.${(date.getMonth() + 1).toString().padStart(2, '0')}.${date.getFullYear()}`;
    
    const threatClass = event.type === 'incident' ? 
        `threat-${event.level}` : 
        'threat-holiday';
    
    const threatText = event.type === 'incident' ? 
        `ОЦЕНКА УГРОЗЫ: ${event.levelText}` : 
        event.levelText;
    
    const vectorsHTML = event.vectors && event.vectors.length > 0 ? `
        <h3>ВЕКТОР АТАКИ</h3>
        <ul>
            ${event.vectors.map(vector => `<li>${vector}</li>`).join('')}
        </ul>
    ` : '';
    
    const measuresHTML = event.measures && event.measures.length > 0 ? `
        <h3>${event.type === 'incident' ? 'ПРИНЯТЫЕ МЕРЫ' : 'РЕКОМЕНДАЦИИ'}</h3>
        <ul>
            ${event.measures.map(measure => `<li>${measure}</li>`).join('')}
        </ul>
    ` : '';
    
    return `
        <div class="event-content">
            <div class="event-header">
                <div class="event-date">${formattedDate} | ${event.date.split(' | ')[1] || 'Историческое событие'}</div>
                <div class="event-threat-tag ${threatClass}">${threatText}</div>
            </div>
            
            <h2 class="event-title">${event.title}</h2>
            
            <div class="event-body">
                <div class="event-description">
                    <p>${event.description}</p>
                    ${vectorsHTML}
                </div>
                
                <div class="event-measures">
                    ${measuresHTML}
                </div>
            </div>
            
            ${event.source ? `
                <div class="event-source">
                    <a href="${event.source}" target="_blank">
                        <i class="fas fa-external-link-alt"></i>
                        ${event.linkText || 'Подробнее в источнике'}
                    </a>
                </div>
            ` : ''}
        </div>
    `;
}

