// Навигация к предыдущему событию
function navigateToPrevEvent() {
    if (!selectedDate) return;
    
    const date = new Date(selectedDate);
    let prevDate = new Date(date);
    prevDate.setDate(prevDate.getDate() - 1);
    
    // Ищем ближайшее предыдущее событие
    while (prevDate.getMonth() === currentMonth && prevDate.getFullYear() === currentYear) {
        const prevDateString = formatDateForDB(prevDate);
        if (events[prevDateString] && checkEventFilter(events[prevDateString])) {
            selectDate(prevDateString);
            scrollToDateInCalendar(prevDateString);
            return;
        }
        prevDate.setDate(prevDate.getDate() - 1);
    }
}

// Навигация к следующему событию
function navigateToNextEvent() {
    if (!selectedDate) return;
    
    const date = new Date(selectedDate);
    let nextDate = new Date(date);
    nextDate.setDate(nextDate.getDate() + 1);
    
    // Ищем ближайшее следующее событие
    while (nextDate.getMonth() === currentMonth && nextDate.getFullYear() === currentYear) {
        const nextDateString = formatDateForDB(nextDate);
        if (events[nextDateString] && checkEventFilter(events[nextDateString])) {
            selectDate(nextDateString);
            scrollToDateInCalendar(nextDateString);
            return;
        }
        nextDate.setDate(nextDate.getDate() + 1);
    }
}

// Прокрутка к дате в календаре
function scrollToDateInCalendar(dateString) {
    const date = new Date(dateString);
    const day = date.getDate();
    
    const dayElement = Array.from(document.querySelectorAll('.calendar-day')).find(dayEl => {
        const dayNumber = dayEl.querySelector('.day-number');
        return dayNumber && parseInt(dayNumber.textContent) === day;
    });
    
    if (dayElement) {
        dayElement.scrollIntoView({ behavior: 'smooth', block: 'nearest', inline: 'center' });
    }
}

// Выбор даты пользователя
function selectUserDate() {
    const userDate = getUserDate();
    if (userDate && userDate.getMonth() === currentMonth && userDate.getFullYear() === currentYear) {
        const userDateString = formatDateForDB(userDate);
        selectDate(userDateString);
        scrollToDateInCalendar(userDateString);
    }
}

// Получение даты пользователя (приведенной к текущему месяцу)
function getUserDate() {
    const now = new Date();
    const userDay = now.getDate();
    
    // Для текущего месяца
    return new Date(currentYear, currentMonth, Math.min(userDay, new Date(currentYear, currentMonth + 1, 0).getDate()));
}

// Форматирование даты для базы данных
function formatDateForDB(date) {
    const year = date.getFullYear();
    const month = String(date.getMonth() + 1).padStart(2, '0');
    const day = String(date.getDate()).padStart(2, '0');
    return `${year}-${month}-${day}`;
}

// Форматирование даты для отображения
function formatDateDisplay(dateString) {
    const date = new Date(dateString);
    const day = date.getDate();
    const month = MONTH_NAMES[date.getMonth()].substring(0, 3);
    return `${day} ${month}`;
}

